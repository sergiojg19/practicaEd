    private void inicializarVentas() {
        int id = 0;
        if (controlador.getVentas().isEmpty()) {
            for (EnumMes mes : EnumMes.values()) {
                Venta v = new Venta();
                v.setMes(mes);
                v.setValor(0.0);
                v.setId(id);
                id++;
                controlador.getSucursal().getVentas().insertar(v);
            }
        }
    }

