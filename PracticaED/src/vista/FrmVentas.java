    private void cargarVenta() throws VacioException, PosicionException {
        if (fila >= 0) {
            venta = controlador.getSucursal().getVentas().get(fila);
            labelMes.setText(venta.getMes().name());
            txtValor.setText(String.valueOf(venta.getValor()));
        } else {
            JOptionPane.showConfirmDialog(null, "Seleccione un mes");
        }
    }

    private void modificar() throws VacioException, PosicionException {
        try {
            venta = controlador.getSucursal().getVentas().get(fila);
            
            Venta aux = new Venta();
            aux.setId(venta.getId());
            aux.setMes(venta.getMes());
            aux.setValor(Double.parseDouble(txtValor.getText()));
            
            controlador.getSucursal().getVentas().modificar(aux, fila);
            dao.modificar(controlador.getSucursal(), controlador.getSucursal().getId());
            JOptionPane.showMessageDialog(null, "Guardado correctamente");
            
            guardarHistorial();
            
            
            limpiar();
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }

    }

    private void guardarHistorial() throws VacioException, PosicionException, IOException, TopeException {
        HIstorial nh = new HIstorial();
        
        colaHistorial = daoHistorial.listar();
        
        nh.setVenta(controlador.getSucursal().getVentas().get( fila));
        nh.setDate(new Date());
          
        if (colaHistorial.getCola().isFull()){
            colaHistorial.dequeue();
        }
        
        colaHistorial.queue(nh);
        
        daoHistorial.guardarCola(colaHistorial);
    }
