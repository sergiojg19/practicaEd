    //Guardar la sucursal ingresada.
    private void guardarSucursal() {      
        if (!txtNombre.getText().trim().isEmpty()) {
            try {
                Sucursal sucursal = new Sucursal();
                sucursal.setNombre(txtNombre.getText().toString());
                sucursal.setId(id);
                
                controlador.getSucursales().insertar(sucursal);
                
                dao.guardar(sucursal);
                
                id++;
                limpiar();
                JOptionPane.showMessageDialog(null, "Se ah guardado", "OK", JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException ex) {
                Logger.getLogger(FrmSucursal.class.getName()).log(Level.SEVERE, null, ex);
            }
           
        } else {
            JOptionPane.showMessageDialog(null, "LLene los campos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

