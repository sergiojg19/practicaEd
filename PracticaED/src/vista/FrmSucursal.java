    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
        int fila = tblTabla.getSelectedRow();
        if (fila >= 0) {
            Sucursal sucursal = new Sucursal();
            if (dao.obtener(fila) != null) {
                sucursal = dao.obtener(fila);
                
                new FrmVentas(null, true, sucursal).setVisible(true);
                
                cargarTabla();
                tblTabla.clearSelection();
            } else {
                JOptionPane.showMessageDialog(null, "No existe esa sucursal", "Error", JOptionPane.ERROR_MESSAGE);
            } 
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una sucursal", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

